const iconOnlySizeClasses: Record<ButtonSize, string> = {
  lg:   'h-9 w-9 p-0 rounded-[var(--radius-8)]',
  md:   'h-8 w-8 p-0 rounded-[var(--radius-8)]',
  sm:   'h-7 w-7 p-0 rounded-[var(--radius-8)]',
  tiny: 'h-6 w-6 p-0 rounded-[var(--radius-6)]',
};

// ─── Component ────────────────────────────────────────────────────────────────

export const Button = forwardRef<HTMLButtonElement, ButtonProps>(({
  variant = 'primary',
  size = 'md',
  iconPosition = 'none',
  inversed = false,
  floating = false,
  loading = false,
  icon,
  fullWidth = false,
  disabled,
  children,
  className = '',
  ...props
}, ref) => {
  const isDisabled = disabled || loading;
  const isIconOnly = iconPosition === 'icon-only';

  return (
    <button
      ref={ref}
      disabled={isDisabled}
      aria-busy={loading}
      className={[
        'inline-flex items-center justify-center font-sans',
        'transition-colors duration-150 select-none whitespace-nowrap',
        variantClasses[variant],
        isIconOnly ? iconOnlySizeClasses[size] : sizeClasses[size],
        floating ? 'shadow-md' : '',
        inversed ? 'brightness-110' : '',
        fullWidth ? 'w-full' : '',
        className,
      ].filter(Boolean).join(' ')}
      {...props}
    >
      {loading && (
        <span
          aria-hidden="true"
          className="inline-block w-4 h-4 border-2 border-current border-t-transparent rounded-full animate-spin shrink-0"
        />
      )}

      {!loading && (iconPosition === 'leading' || isIconOnly) && icon && (
        <span aria-hidden="true" className="shrink-0 flex items-center">
          {icon}
        </span>
      )}

      {!isIconOnly && children && (
        <span>{children}</span>
      )}

      {!loading && iconPosition === 'trailing' && icon && (
        <span aria-hidden="true" className="shrink-0 flex items-center">
          {icon}
        </span>
      )}
    </button>
  );
});

Button.displayName = 'Button';
export default Button;
