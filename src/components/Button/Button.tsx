import { forwardRef, ButtonHTMLAttributes } from 'react';

// ─── Types ────────────────────────────────────────────────────────────────────
// Matches Prism DS Button component set (node: 361:3849) exactly:
//   Style:         Primary | Secondary | Subtle | Primary Destructive | Secondary Destructive | Warning
//   Size:          Large | Medium (Default) | Small | Tiny
//   Icon Position: None | Leading | Trailing | Icon Only
//   Inversed:      No | Yes
//   Floating:      No | Yes

export type ButtonStyle =
  | 'primary'
  | 'secondary'
  | 'subtle'
  | 'primary-destructive'
  | 'secondary-destructive'
  | 'warning';

export type ButtonSize = 'lg' | 'md' | 'sm' | 'tiny';
export type ButtonIconPosition = 'none' | 'leading' | 'trailing' | 'icon-only';

export interface ButtonProps extends ButtonHTMLAttributes<HTMLButtonElement> {
  /** Maps to Figma Style property */
  variant?: ButtonStyle;
  /** Maps to Figma Size property */
  size?: ButtonSize;
  /** Maps to Figma Icon Position property */
  iconPosition?: ButtonIconPosition;
  /** Maps to Figma Inversed — use on dark/colored backgrounds */
  inversed?: boolean;
  /** Maps to Figma Floating — elevated shadow treatment */
  floating?: boolean;
  /** Loading state — shows spinner, disables interaction */
  loading?: boolean;
  /** Icon element for leading/trailing/icon-only positions */
  icon?: React.ReactNode;
  /** Expand button to fill container width */
  fullWidth?: boolean;
}

